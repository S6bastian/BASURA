#include <bits/stdc++.h>
using namespace std;

#define ll long long

void toBin(vector<ll> &bits, int x){
	for(int i = 0; x > 0;i++){
		bits[i] += x%2;
		x /= 2;
	}
}

void solve(){
	ll n;
	cin >> n;
	
	vector<ll> a(n);
	
	vector<ll> bits(31);
	
	for(auto &u : a) cin >> u;
	
	ll max = 0;
	
	for(auto u : a){
		ll c = 0;
		for(auto v : a){
			c += (u^v);
		}
		if(c > max) max = c;
	}
	
	
	cout << max << endl;
}

